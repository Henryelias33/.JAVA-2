public class Solucao1e {
    public static void main(String[] args) {
        // Definindo um número para verificar
        int numero = 30;

        // Verificando se o número está entre 20 e 90
        if (numero >= 20 && numero <= 90) {
            System.out.println("O número " + numero + " está entre 20 e 90.");
        } else {
            System.out.println("O número " + numero + " não está entre 20 e 90.");
        }
    }
}
